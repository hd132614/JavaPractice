public class JavaVariableSize {

    public static void main(String[] args) {
        System.out.println("Byte type size      : " + Byte.SIZE / 8 + "byte");
        System.out.println("Character type size : " + Character.SIZE / 8 + "byte");
        System.out.println("Short type size     : " + Short.SIZE / 8 + "byte");
        System.out.println("Integer type size   : " + Integer.SIZE / 8 + "byte");
        System.out.println("Double type size    : " + Double.SIZE / 8 + "byte");
        System.out.println("Float type size     : " + Float.SIZE / 8 + "byte");
        System.out.println("Long type size      : " + Long.SIZE / 8 + "byte");
    }
}
